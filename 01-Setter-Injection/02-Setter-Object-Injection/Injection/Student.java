package com.Pentagon;

public class Student {

    private Mobile m;
    private int id;
    private String name;

    public Mobile getM() {
        return m;
    }

    public void setM(Mobile m) {
        this.m = m;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
